import { Coordinate3d } from "../../Coordinate"
import { BlockDiagramSchema } from "./DiagramSchemaTypes"


///////////////////////////////////////////////////////////////////////////////
//  1. Block Diagram Document  ////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type BlockDiagramDocumentExport = {
    api_version: string,
    name: string,
    schema: BlockDiagramSchema,
    page: string,
    objects: DiagramObjectExport[]
}


///////////////////////////////////////////////////////////////////////////////
//  2. Diagram Object  ////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DiagramObjectExport = {
    id: string,
    x: number,
    y: number,
    template: string,
    children: string[]
}


///////////////////////////////////////////////////////////////////////////////
//  4. Diagram Anchor  ////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DiagramAnchorExport = DiagramObjectExport & {
    position: number
}


///////////////////////////////////////////////////////////////////////////////
//  5. Dictionary Block  //////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DictionaryBlockExport = DiagramObjectExport & {
    fields: { [key: string] : any }
}


///////////////////////////////////////////////////////////////////////////////
//  6. Page  //////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type PageExport = DiagramObjectExport & {
    camera: Coordinate3d
}
