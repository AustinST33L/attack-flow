import { BlockDiagramSchema } from "."
import { 
    CameraLocation,
    DiagramObjectModel
} from "../DiagramModelTypes"


///////////////////////////////////////////////////////////////////////////////
//  1. Block Diagram Document  ////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type BlockDiagramDocumentExport = {
    api_version: string,
    name: string,
    schema: BlockDiagramSchema,
    page: string,
    objects: DiagramObjectExport[]
}


///////////////////////////////////////////////////////////////////////////////
//  2. Diagram Object  ////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DiagramObjectValues = {
    id?: string,
    x?: number,
    y?: number,
    attrs?: number,
    template: string,
    children?: DiagramObjectModel[]
}

export type DiagramObjectExport = ValuesToExportType<DiagramObjectValues>;


///////////////////////////////////////////////////////////////////////////////
//  3. Diagram Anchor  ////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DiagramAnchorValues = DiagramObjectValues & {
    angle?: number
}

export type DiagramAnchorExport = ValuesToExportType<DiagramAnchorValues>


///////////////////////////////////////////////////////////////////////////////
//  4. Dictionary Line  ///////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DictionaryLineValues = DiagramObjectValues & {
    fields?: { [key: string] : any }
}

export type DictionaryLineExport = ValuesToExportType<DictionaryLineValues>


///////////////////////////////////////////////////////////////////////////////
//  5. Dictionary Block  //////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type DictionaryBlockValues = DiagramObjectValues & {
    fields?: { [key: string] : any }
}

export type DictionaryBlockExport = ValuesToExportType<DictionaryBlockValues>;


///////////////////////////////////////////////////////////////////////////////
//  6. Page  //////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


export type PageValues = DiagramObjectValues & {
    location?: CameraLocation
}

export type PageExport = ValuesToExportType<PageValues>;


///////////////////////////////////////////////////////////////////////////////
//  14. Type Helpers  /////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////


/**
 * Values to export type
 */
type ValuesToExportType<T extends DiagramObjectValues> = Required<
    SubstituteType<T, DiagramObjectModel[], string[]>
>

/**
 * Substitutes all types of type `A` in type `T` with type `B`.
 * ({@link https://stackoverflow.com/a/59833891 Source})
 */
type SubstituteType<T, A, B> =
    T extends A
    ? B
    : T extends {}
    ? { [K in keyof T]: SubstituteType<T[K], A, B> }
    : T;
